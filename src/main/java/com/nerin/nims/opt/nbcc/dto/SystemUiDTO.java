public class SystemUiDTO {

    private String systemUiCode;
    private String systemUiName;
    private String systemUiUrl;

    public String getSystemUiCode() {
        return systemUiCode;
    }

    public void setSystemUiCode(String systemUiCode) {
        this.systemUiCode = systemUiCode;
    }

    public String getSystemUiName() {
        return systemUiName;
    }

    public void setSystemUiName(String systemUiName) {
        this.systemUiName = systemUiName;
    }

    public String getSystemUiUrl() {
        return systemUiUrl;
    }

    public void setSystemUiUrl(String systemUiUrl) {
        this.systemUiUrl = systemUiUrl;
    }
}
